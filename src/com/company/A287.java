package com.company;
/*
Task 287

В одном очень известном университете один очень известный профессор очень быстро произносил свои лекции, так, что ничего
невозможно было разобрать. Но недавно студент Вилли решил провести исследование по изучению словарного запаса профессора.
 С этой целью он даже посетил одну лекцию и записал всё сказанное на ней на диктофон. Затем, прокручивая дома запись с
 десятикратным замедлением, Вилли смог записать всё, что сказал профессор.

Но вот незадача – профессор говорил так быстро, что, даже прослушивая замедленную запись, нельзя было точно сказать, где
 он делал паузы между словами. Таким образом, у Вилли есть некоторый текст, состоящий из n маленьких английских букв –
 лекция, которая была прочитана профессором. Теперь Вилли хочет знать, какое количество различных слов длины m мог
 использовать в своей лекции профессор.

-----Входные данные--------
Первая строка входного файла INPUT.TXT содержит два числа n и m (1 ≤ m ≤ n ≤ 100) – длина лекции и длина слова.
Вторая строка содержит n английских символов - текст лекции профессора.

------Выходные данные--------
В выходной файл OUTPUT.TXT выведите одно число – количество слов длины m, которые профессор мог использовать в своей лекции.

==========================================================================
	3 1
    abc             	3
==========================================================================
    10 3
    bbaabbbabb	        6
==========================================================================

 */

import java.io.PrintWriter;
import java.util.Scanner;

public class A287 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        PrintWriter pw = new PrintWriter(System.out);

        int nLengthOfString = sc.nextInt();
        int mLengthOfSubstring = sc.nextInt();
        String s = sc.next();
        int count = 0;
        for (int i = 0; i < nLengthOfString; i++) {
            for (int j = i; j <mLengthOfSubstring ; j++) {
                if (s.contains(s.substring(j,mLengthOfSubstring)))
                    count++;
            }
        }
        pw.println(count);
        pw.flush();
    }
}
